class Errors
{

	//-----------------------------------------------------------------------------------------------
	// Members
	//-----------------------------------------------------------------------------------------------
	private $errorMsg;			     // error messages
	private $error;					// errors found in instance
	
	
	//-----------------------------------------------------------------------------------------------
	// Constructor
	//-----------------------------------------------------------------------------------------------
	//	return void
	//-----------------------------------------------------------------------------------------------
	public function __construct()
	{
		// language error messages
		$this->errorMsg = $GLOBALS['ERROR_MSG'];
	}
	
	
	
	//-----------------------------------------------------------------------------------------------
	// Add new error to array
	//-----------------------------------------------------------------------------------------------
	//	int $error
	//	return void
	//-----------------------------------------------------------------------------------------------
	public function add($error)
	{
		$this->error[] = $error;
	}
	
	
	//-----------------------------------------------------------------------------------------------
	// Display Errors
	//-----------------------------------------------------------------------------------------------
	//	return void
	//-----------------------------------------------------------------------------------------------
	public function display()
	{
		// print each error
		foreach($this->error as $v)
			echo $this->getMsg($v), '<br />';
	}
	
	
	//-----------------------------------------------------------------------------------------------
	// Check if there's an error
	//-----------------------------------------------------------------------------------------------
	//	return bool
	//-----------------------------------------------------------------------------------------------
	public function isEmpty()
	{
		if(count($this->error))
			return false;
		
		return true;
	}
	
	
	//-----------------------------------------------------------------------------------------------
	//	Get error results
	//-----------------------------------------------------------------------------------------------
	//	int $error 	: error code
	//	return string
	//-----------------------------------------------------------------------------------------------
	public function getMsg($error)
	{
		if(key_exists($error, $this->errorMsg))
			return $this->errorMsg[$error];
		
		return $this->errorMsg[1000];
		
	}
}
