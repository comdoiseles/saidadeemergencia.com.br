class Utils
{
	//-----------------------------------------------------------------------------------------------
	//	Init Vars
	//-----------------------------------------------------------------------------------------------
	//	array &$array
	//	array $vars
	//	bool	$bDisableMQ			// disable magic quotes
	//	return void
	//-----------------------------------------------------------------------------------------------
	public function initVars(&$array, $vars, $bDisableMQ = 0)
	{
		foreach($vars as $v)
			if(!isset($array[$v]))
				$array[$v] = '';
				
		if(!$bDisableMQ)
			if(get_magic_quotes_gpc())
				foreach($array as $k => $v)
  				$array[$k] = stripslashes($v);
	}
}
