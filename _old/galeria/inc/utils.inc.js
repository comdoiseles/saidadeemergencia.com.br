//-------------------------------------------------------------------------------------------------
// Create XMLHttp
//-------------------------------------------------------------------------------------------------
//	return obj
//-------------------------------------------------------------------------------------------------
function getXMLHttpRequest() {
	var xmlHttp = '';
	
	// Real browsers
	if(window.XMLHttpRequest)
		xmlHttp = new XMLHttpRequest();
	
	// IE
	if(window.ActiveXObject)
		xmlHttp = new ActiveXObject("Microsoft.XMLHTTP");
		
	return xmlHttp;
}


