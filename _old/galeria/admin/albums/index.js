//-------------------------------------------------------------------------------------------------
// Globals
//-------------------------------------------------------------------------------------------------
var xmlHttp;


//-------------------------------------------------------------------------------------------------
//	Window - onload
//-------------------------------------------------------------------------------------------------
window.onload = function()
{
	// init xmlHttp
	xmlHttp = getXMLHttpRequest();
}


//-------------------------------------------------------------------------------------------------
//	Delete Album
//-------------------------------------------------------------------------------------------------
//	int			albumId
//	string	sMsg
//	return 	void
//-------------------------------------------------------------------------------------------------
function delAlbum(albumId, sMsg)
{
	if(!confirm(sMsg))
		return;
	
	var url = "delAlbum.php?albumId=" + albumId;
	
	xmlHttp.open("GET", url, true);
	xmlHttp.onreadystatechange = delAlbumCallback;
	xmlHttp.send(null);
	
}


//-------------------------------------------------------------------------------------------------
//	Delete Album Callback
//-------------------------------------------------------------------------------------------------
//	return void
//-------------------------------------------------------------------------------------------------
function delAlbumCallback()
{
	if(xmlHttp.readyState != 4)
		return;
		
	if(xmlHttp.status != 200)
		return;
	
	// reload page
	window.location = "index.php";
}
